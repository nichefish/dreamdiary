    LOG_DT DATETIME,
    LOG_USER_ID VARCHAR(20),
    --
    ACTVTY_CTGR_CD VARCHAR(50),
    ACTION_TY_CD VARCHAR(50),
    --
    URL VARCHAR(400),
    CN VARCHAR(400),
    RSLT CHAR(1),
    RSLT_MSG VARCHAR(500),
    EXCEPTION_NM VARCHAR(100),
    EXCEPTION_MSG VARCHAR(4000)
) COMMENT = '시스템 로그';